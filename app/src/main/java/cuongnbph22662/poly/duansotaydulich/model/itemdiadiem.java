package cuongnbph22662.poly.duansotaydulich.model;

public class itemdiadiem {
    private int img ;
    private String tittle ;

    public itemdiadiem(int img, String tittle) {
        this.img = img;
        this.tittle = tittle;
    }

    public int getImg() {
        return img;
    }

    public void setImg(int img) {
        this.img = img;
    }

    public String getTittle() {
        return tittle;
    }

    public void setTittle(String tittle) {
        this.tittle = tittle;
    }
}
